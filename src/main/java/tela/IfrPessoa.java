            JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

            // limpar campos
            tfdNome.setText("");
            tfdEmail.setText("");
            tfdUsuario.setText("");
            tfdSenha.setText("");
            tfdCpf.setText("");

            // ajustar foco
            tfdNome.requestFocus();

            // atualiza ID
