    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Produto produto = new Produto();
        produto.setValor(tfdValor.getText());
        produto.setVolume(tfdVolume.getText());
        produto.setRecipiente(tfdRecipiente.getText());
        produto.setMarca(tfdMarca.getText());
        produto.setNome(tfdNome.getText());
        produto.setCod(cod);

        // salvar
        ProdutoDAO apDAO = new ProdutoDAO();
        
        if (tfdNome.getText().equals("") || tfdMarca.getText().isEmpty() || tfdRecipiente.getText().isEmpty() || tfdVolume.getText().equals("") || tfdValor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Os campos obrigatórios não podem ser nulos!");
            
            tfdNome.setText("");
            tfdMarca.setText("");
            tfdRecipiente.setText("");
            tfdVolume.setText("");
            tfdValor.setText("");
            
            tfdNome.requestFocus();
            
        } else {
        boolean retorno = false;
        
        if (produto.getCod() == 0) {
            retorno = apDAO.salvar(produto);
        } else {
            retorno = apDAO.salvar(produto);
        }
        
        if (retorno == true) {
            JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

            tfdNome.setText("");
            tfdMarca.setText("");
            tfdRecipiente.setText("");
            tfdVolume.setText("");
            tfdValor.setText("");
            
            tfdNome.requestFocus();

            cod = 0;
            
            new ProdutoDAO().popularTabela(tblProduto, "");
        } else {
            JOptionPane.showMessageDialog(null, "Problemas ao salvar registro!");
        }
      }

        /*if (apDAO.salvar(produto)) {
            // exibir msg
            JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

            // limpar campos
            tfdNome.setText("");
            tfdMarca.setText("");
            tfdRecipiente.setText("");
            tfdVolume.setText("");


            // ajustar foco
            tfdNome.requestFocus();

            // atualiza ID
            cod = 0;
        } else {
            JOptionPane.showMessageDialog(null, "Problemas ao salvar registro!");
        }*/

    }//GEN-LAST:event_btnSalvarActionPerformed

