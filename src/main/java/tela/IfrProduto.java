    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idString = String.valueOf(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0));

        cod = Integer.parseInt(idString);

        ProdutoDAO apDAO = new ProdutoDAO();

        Produto ap = apDAO.consultarId(cod);

        if (ap != null) {
            tfdNome.setText(ap.getNome());
            tfdMarca.setText(ap.getMarca());
            tfdVolume.setText(ap.getVolume());
            tfdValor.setText(ap.getValor());
            tfdRecipiente.setText(ap.getRecipiente());
            tfdCod.setText(String.valueOf(ap.getCod()));

            jTabbedPane1.setSelectedIndex(0);
        } else {
            System.out.println("Erro na consulta");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idString = String.valueOf(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0));

        cod = Integer.parseInt(idString);

        ProdutoDAO apDAO = new ProdutoDAO();

        if (JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?") == JOptionPane.OK_OPTION) {
            if (apDAO.excluir(cod)) {
                JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                new ProdutoDAO().popularTabela(tblProduto, tfdBusca.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Problemas ao excluir registro!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


